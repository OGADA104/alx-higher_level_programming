#!/usr/bin/python3

class Square:
    """
    This is a class that defines a square.

    Attributes:
        __size (int): The size (side length) of the square.
    """

    def __init__(self, size):
        """
        Initializes a square object with a specific size.

        Args:
            size (int): The size (side length) of the square.
        """
        self.__size = size
